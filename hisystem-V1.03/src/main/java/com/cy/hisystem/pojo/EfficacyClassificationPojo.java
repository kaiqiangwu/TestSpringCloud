package com.cy.hisystem.pojo;

import java.io.Serializable;

/**
 * 	对应数据库中表his_efficacy_classification的实体类
 * 
 * @author 黄德傲
 *
 */
public class EfficacyClassificationPojo implements Serializable {
	private static final long serialVersionUID = 1L;
	/** 表id */
	private String id;
	/** 记录创建时间 */
	private String createDatetime;
	/** 药品功效 */
	private String efficacyClassification;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getCreateDatetime() {
		return createDatetime;
	}

	public void setCreateDatetime(String createDatetime) {
		this.createDatetime = createDatetime;
	}

	public String getEfficacyClassification() {
		return efficacyClassification;
	}

	public void setEfficacyClassification(String efficacyClassification) {
		this.efficacyClassification = efficacyClassification;
	}

	@Override
	public String toString() {
		return "EfficacyClassificationPojo [id=" + id + ", createDatetime=" + createDatetime
				+ ", efficacyClassification=" + efficacyClassification + "]";
	}

	public EfficacyClassificationPojo(String id, String createDatetime, String efficacyClassification) {
		super();
		this.id = id;
		this.createDatetime = createDatetime;
		this.efficacyClassification = efficacyClassification;
	}

	public EfficacyClassificationPojo() {
		super();
	}
}
