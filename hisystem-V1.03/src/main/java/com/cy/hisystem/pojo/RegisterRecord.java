package com.cy.hisystem.pojo;

import java.io.Serializable;

/**
 * 
 * @author 陈旭升
 *此类用于挂号记录查询实体类
 */
public class RegisterRecord implements Serializable {
/**
 * 
  */
	private static final long serialVersionUID = -5208688800416263292L;
	private String cardId;//就诊卡id
	private String department;//科室
	private String name;//姓名
	private String doctor;//就诊医生
	private String registerType;//挂号类型
	private String createDateTime;//创建时间
	private String createPerson;//
	private String createPersonEmail;//标识邮箱
	
@Override
	public String toString() {
		return "RegisterRecord [cardId=" + cardId + ", department=" + department + ", name=" + name + ", doctor="
				+ doctor + ", registerType=" + registerType + ", createDateTime=" + createDateTime + ", createPerson="
				+ createPerson + ", createPersonEmail=" + createPersonEmail + "]";
	}
public String getCardId() {
		return cardId;
	}
	public void setCardId(String cardId) {
		this.cardId = cardId;
	}
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDoctor() {
		return doctor;
	}
	public void setDoctor(String doctor) {
		this.doctor = doctor;
	}
	public String getRegisterType() {
		return registerType;
	}
	public void setRegisterType(String registerType) {
		this.registerType = registerType;
	}
	public String getCreateDateTime() {
		return createDateTime;
	}
	public void setCreateDateTime(String createDateTime) {
		this.createDateTime = createDateTime;
	}
	public String getCreatePerson() {
		return createPerson;
	}
	public void setCreatePerson(String createPerson) {
		this.createPerson = createPerson;
	}
	public String getCreatePersonEmail() {
		return createPersonEmail;
	}
	public void setCreatePersonEmail(String createPersonEmail) {
		this.createPersonEmail = createPersonEmail;
	}

}
