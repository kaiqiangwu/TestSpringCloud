package com.cy.hisystem.pojo;

import java.io.Serializable;

/**
 * 就诊卡实体类
 * 本类负责记载就诊卡信息
 * @author 李溢
 *
 */
public class IdCardPojo implements Serializable{
    /**序列化编号*/
    private static final long serialVersionUID = -280408653805302618L;
    /**姓名*/
    private String name;
    /**身份证号*/
    private String idNumber;
    /**性别*/
    private String sex;
    /**民族*/
    private String nationality;
    /**出生日期*/
    private String birth;
    /**住址*/
    private String adress;
    /**卡号*/
    private String cardNumber;
    /**手机号*/
    private String phoneNumber;
    
    /**
     * 无参构造
     */
    public IdCardPojo() {
    }

    /**
     * 全参构造
     */

    public IdCardPojo(String name, String idNumber, String sex, String nationality, String birth, String adress,
            String cardNumber, String phoneNumber) {
        super();
        this.name = name;
        this.idNumber = idNumber;
        this.sex = sex;
        this.nationality = nationality;
        this.birth = birth;
        this.adress = adress;
        this.cardNumber = cardNumber;
        this.phoneNumber = phoneNumber;
    }

    /**
     * 重写toString
     */
    

    @Override
    public String toString() {
        return "IdCardPojo [name=" + name + ", idNumber=" + idNumber + ", sex=" + sex + ", nationality=" + nationality
                + ", birth=" + birth + ", adress=" + adress + ", cardNumber=" + cardNumber + ", phoneNumber="
                + phoneNumber + "]";
    }

    /**
     * get set 方法
     * @return
     */


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIdNumber() {
        return idNumber;
    }

    public void setIdNumber(String idNumber) {
        this.idNumber = idNumber;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getNationality() {
        return nationality;
    }

    public void setNationality(String nationality) {
        this.nationality = nationality;
    }

    public String getBirth() {
        return birth;
    }

    public void setBirth(String birth) {
        this.birth = birth;
    }

    public String getAdress() {
        return adress;
    }

    public void setAdress(String adress) {
        this.adress = adress;
    }

    public String getCardNumber() {
        return cardNumber;
    }

    public void setCardNumber(String cardNumber) {
        this.cardNumber = cardNumber;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }


  
    

}
