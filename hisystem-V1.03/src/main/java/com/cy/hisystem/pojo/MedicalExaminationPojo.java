package com.cy.hisystem.pojo;

import java.io.Serializable;

/**
 * @author 张孙传
 * 实体类
 * 检查情况的信息封装
 */
public class MedicalExaminationPojo implements Serializable{
	/**
	 * 序列化id
	 */
	private static final long serialVersionUID = -2754148548126543741L;
	/**身体检查*/
	private String id;
	/**创建时间*/
	private String createDatetime;
	/**血压*/
	private String bloodPressure;
	/**体温*/
	private String bodyTemperature;
	/**心率*/
	private String heartRate;
	/**脉搏*/
	private String pulse;
	/**检查费用*/
	private Double examinationCost;
	/**药方号码*/
	private String prescriptionNum;
	/**操作人员*/
	private String examinationOperator;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCreateDatetime() {
		return createDatetime;
	}
	public void setCreateDatetime(String createDatetime) {
		this.createDatetime = createDatetime;
	}
	public String getBloodPressure() {
		return bloodPressure;
	}
	public void setBloodPressure(String bloodPressure) {
		this.bloodPressure = bloodPressure;
	}
	public String getBodyTemperature() {
		return bodyTemperature;
	}
	public void setBodyTemperature(String bodyTemperature) {
		this.bodyTemperature = bodyTemperature;
	}
	public String getHeartRate() {
		return heartRate;
	}
	public void setHeartRate(String heartRate) {
		this.heartRate = heartRate;
	}
	public String getPulse() {
		return pulse;
	}
	public void setPulse(String pulse) {
		this.pulse = pulse;
	}
	public Double getExaminationCost() {
		return examinationCost;
	}
	public void setExaminationCost(Double examinationCost) {
		this.examinationCost = examinationCost;
	}
	public String getPrescriptionNum() {
		return prescriptionNum;
	}
	public void setPrescriptionNum(String prescriptionNum) {
		this.prescriptionNum = prescriptionNum;
	}
	public String getExaminationOperator() {
		return examinationOperator;
	}
	public void setExaminationOperator(String examinationOperator) {
		this.examinationOperator = examinationOperator;
	}
	@Override
	public String toString() {
		return "MedicalExaminationPojo [id=" + id + ", createDatetime=" + createDatetime + ", bloodPressure="
				+ bloodPressure + ", bodyTemperature=" + bodyTemperature + ", heartRate=" + heartRate + ", pulse="
				+ pulse + ", examinationCost=" + examinationCost + ", prescriptionNum=" + prescriptionNum
				+ ", examinationOperator=" + examinationOperator + "]";
	}
	

}
