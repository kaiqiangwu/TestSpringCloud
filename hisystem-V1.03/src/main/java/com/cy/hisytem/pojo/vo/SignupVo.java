package com.cy.hisytem.pojo.vo;

import java.io.Serializable;

import org.omg.CORBA.PUBLIC_MEMBER;

public class SignupVo implements Serializable{
	
	private static final long serialVersionUID = -7210407944367166802L;

	/** 用户名 */
	private String username; 
	
	/** 注册邮箱 */
	private String email;
	
	/** 加密后密码 */
	private String password;
	
	/** 角色编号 */
	private Integer roleValue;
	
    /**邮箱状态*/
    private Integer emailStatus;
    
    /**验证码*/
    private String validateCode;
	
	/**盐值*/
    private String salt;
    
    /**用户原密码*/
    private String plainPassword;
    
    /** 用户表id */
    private String id;
    
    /** 创建时间 */
    public String createDatetime;

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public Integer getRoleValue() {
		return roleValue;
	}

	public void setRoleValue(Integer roleValue) {
		this.roleValue = roleValue;
	}

	public String getSalt() {
		return salt;
	}

	public void setSalt(String salt) {
		this.salt = salt;
	}

	public String getPlainPassword() {
		return plainPassword;
	}

	public void setPlainPassword(String plainPassword) {
		this.plainPassword = plainPassword;
	}

	
	
	public Integer getEmailStatus() {
		return emailStatus;
	}

	public void setEmailStatus(Integer emailStatus) {
		this.emailStatus = emailStatus;
	}

	public String getValidateCode() {
		return validateCode;
	}

	public void setValidateCode(String validateCode) {
		this.validateCode = validateCode;
	}

	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	
	
	public String getCreateDatetime() {
		return createDatetime;
	}

	public void setCreateDatetime(String createDatetime) {
		this.createDatetime = createDatetime;
	}

	@Override
	public String toString() {
		return "SignupVo [username=" + username + ", email=" + email + ", password=" + password + ", roleValue="
				+ roleValue + ", emailStatus=" + emailStatus + ", validateCode=" + validateCode + ", salt=" + salt
				+ ", plainPassword=" + plainPassword + ", id=" + id + "]";
	}

	

    
    
    
    
}
